public class FlyingHorse {
    private String wing;
    private String color;

    public FlyingHorse() {
        this.wing = wing;
        this.color = color;
    }
    public void fly(){
        System.out.println("This imaginary object has ability to fly ");
    }

    public String getWing() {
        return wing;
    }

    public void setWing(String wing) {
        this.wing = wing;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
    
    
    
}
